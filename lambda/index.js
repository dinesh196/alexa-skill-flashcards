const Alexa = require('ask-sdk-core');
const content = require('content');
const books = require('books')

const LaunchRequestHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'LaunchRequest';
    },
    handle(handlerInput) {
        const speakOutput = 'Hi. Welcome to the chegg app. What would you like to try?';

        return handlerInput.responseBuilder
            .speak(speakOutput)
            .reprompt(speakOutput)
            .getResponse();
    }
};

const HelloWorldIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'HelloWorldIntent';
    },
    handle(handlerInput) {
        const speakOutput = content.sample1[0].cards[2];

        return handlerInput.responseBuilder
            .speak(speakOutput)
            //.reprompt('add a reprompt if you want to keep the session open for the user to respond')
            .getResponse();
    }
};

const fetchFlashcardsIntentHandler = {
    canHandle(handlerInput) {
        return Alexa.getRequestType(handlerInput.requestEnvelope) === 'IntentRequest'
            && Alexa.getIntentName(handlerInput.requestEnvelope) === 'fetchFlashcards';
    },
    handle(handlerInput) {
        let speakOutput = `Here are all of your flashcards`;
        
        let intent = handlerInput.requestEnvelope.request.intent;
        let subject = intent.slots.subject.value;
        
        if(subject) {
            speakOutput += ` of ${subject} : `;
            const subjectCards = content.sample1.find((sub) => {
                return sub.subjectName === subject;
            })
            let speechOutput = `You have a total of ${subjectCards.cards.length} cards. ` 
            
            subjectCards.cards.forEach( subCard => {
                speakOutput += ` ${subCard} ,  `;
            })
            // speakOutput += `${subjectCards.cards[0]}`
            
            speechOutput += speakOutput;
            return handlerInput.responseBuilder
            .speak(speechOutput)
            //.reprompt('add a reprompt if you want to keep the session open for the user to respond')
            .getResponse();
        } else {
            return handlerInput.responseBuilder
            .speak(speakOutput)
            //.reprompt('add a reprompt if you want to keep the session open for the user to respond')
            .getResponse();
        }
        

        
    }
}



